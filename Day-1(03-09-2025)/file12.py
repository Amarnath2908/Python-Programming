def add(a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b

def pow(a,b):
    return a**b

def fld(a,b):
    return a//b

def rem(a,b):
    return a%b  

